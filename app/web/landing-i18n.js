  function applyLang(lang) {
    var dict = I18N[lang] || I18N.fa;
    document.documentElement.lang = lang;
    document.documentElement.dir = dict._dir;

    var nodes = document.querySelectorAll("[data-i18n]");
    for (var i = 0; i < nodes.length; i++) {
      var key = nodes[i].getAttribute("data-i18n");
      if (dict[key] != null) {
        nodes[i].textContent = dict[key];
      }
    }

    var buttons = document.querySelectorAll(".langSwitch button");
    for (var j = 0; j < buttons.length; j++) {
      if (buttons[j].getAttribute("data-lang") === lang) {
        buttons[j].classList.add("langActive");
      } else {
        buttons[j].classList.remove("langActive");
      }
    }

    try { localStorage.setItem("nextrade_lang", lang); } catch (e) {}
  }

  function init() {
    var saved = "fa";
    try { saved = localStorage.getItem("nextrade_lang") || "fa"; } catch (e) {}
    if (!I18N[saved]) saved = "fa";
    applyLang(saved);

    var buttons = document.querySelectorAll(".langSwitch button");
    for (var i = 0; i < buttons.length; i++) {
      buttons[i].addEventListener("click", function () {
        applyLang(this.getAttribute("data-lang"));
      });
    }
  }

  if (document.readyState === "loading") {
    document.addEventListener("DOMContentLoaded", init);
  } else {
    init();
  }
